                    <h3>Water</h3>
                    <p>Air pollution is also capable of contaminating large bodies of water such as rivers, lakes, or
                        streams. Nitrogen compounds in air pollution are partly the cause of algal blooms, and can also
                        contribute to water bodies becoming more acidic. When fossil fuels are burned, sulfur dioxide
                        and nitrogen oxides are released into the atmosphere. Both of these air pollutants dissolve to
                        form acidic aquatic environments. The acidic water vapor then evaporates and condenses into
                        clouds and inevitably then falls as precipitation, often refereed to acid rain. This decreases
                        water quality not only for fish but also for the animals that rely on that body of water to
                        survive, even humans.</p>
