                        <p>Changes in weather patterns as a result of rising temperatures can result in less rainfall
                            than normal which causes droughts. This causes some very bad effects such as crops wilting
                            and dying, animals struggling to survive from dehydration and if these conditions continue
                            for long periods of time people start to suffer from dehydration and starvation. Cape Town
                            recently experienced a very severe drought. To ensure all citizens had water, restrictions
                            were implemented and families were limited to small amounts of water for their daily
                            household chores.</p>
