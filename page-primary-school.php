                    </div>
                    <div class="info">
                        <p>Because temperatures have increased so much, the large pieces and islands of ice (glaciers)
                            at the poles melt much faster. Animals like polar bears and seals live on this ice, and when
                            it disappears they struggle to survive. The melting of this ice also means there is more
                            water in the oceans and this causes sea levels to rise. Rising sea levels may result in
                            places close to the coast being flooded. Islands countries such as the Maldives are
                            particularly vulnerable.</p>
                    </div>
                </div>
