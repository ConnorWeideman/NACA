                    </div>
                    <div class="info">
                        <p>When temperatures rise we know that weather patterns change, we call this climate change.
                            When weather patterns change, the amount of rain that a place experiences could potentially
                            also change. Sometimes this means more rainfall than normal, which could result in floods.
                            Floods can be very dangerous as they wash houses, cars and buildings away, sometimes if
                            people can not swim or if the water currents are very strong they can drown. Floods are also
                            bad for farmers as they destroy crops and therefore reduces the amount of food available for
                            us to eat. Places like Durban and the Eastern Cape have experienced floods.</p>
